float Opzx3Alg::process(std::array<OpGetSampleFunc, 4>& gsFuncs, std::array<OpFbPushBack, 4>& fpbFuncs, float pmLfoVal) const {
    // ========================================================
    // 2. オペレーターの処理とルーティング
    // ========================================================
    float out1 = 0.0f, out2 = 0.0f, out3 = 0.0f, out4 = 0.0f;

    // 安全のため 0〜7 の範囲に丸める
    int algIndex = std::clamp(idx, 0, 7);
    const auto& r = routings[algIndex];

    // =================================================================
    // OP1 (入力なし。フィードバックは内部で解決される)
    // =================================================================
    gsFuncs[0](out1, 0.0f, pmLfoVal);

    // =================================================================
    // OP2 (入力: OP1)
    // =================================================================
    float in2 = out1 * r.in2_1;
    gsFuncs[1](out2, in2, pmLfoVal);

    // フィードバック指定があれば OP2->OP1 にフィードバックをプッシュ
    if (r.fb2_1) {
        fpbFuncs[0](out2);
    }

    // =================================================================
    // OP3 (入力: OP1, OP2)
    // =================================================================
    float in3 = (out1 * r.in3_1) + (out2 * r.in3_2);
    gsFuncs[2](out3, in3, pmLfoVal);

    // =================================================================
    // OP4 (入力: OP1, OP2, OP3)
    // =================================================================
    float in4 = (out1 * r.in4_1) + (out2 * r.in4_2) + (out3 * r.in4_3);
    gsFuncs[3](out4, in4, pmLfoVal);

    // =================================================================
    // Final Output
    // =================================================================
    return ((out1 * r.out_1) + (out2 * r.out_2) + (out3 * r.out_3) + (out4 * r.out_4)) * 0.25f;
}

bool Opzx3Alg::isUseExtFb() const {
    if (idx == 1 || idx == 5 || idx == 7 || idx == 11) return true; // 4OP: OP1->OP0 FB
    if (idx == 17 || idx == 21) return true; // 3OP: OP2->OP0 FB
    if (idx == 25) return true; // 2OP: OP1->OP0 FB
    return false;
}
